{
    /**
     * Display a listing of the resource.
     *
     * @return \Illuminate\Http\Response
     */
    public function index()
    {
        $mata_pelajaran = Lesson::get();
        return view('pages.admin.data-pelajaran',[
            'mata_pelajaran' => $mata_pelajaran
        ]);
    }

    /**
